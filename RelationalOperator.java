public class RelationalOperator {
    public static void main(String[] args) {
        int num1 = 9;
        int num2 = 8;
        System.out.println(num2>num1);
        System.out.println(num2<num1);
        System.out.println(num1==num2);
        System.out.println(num1!=num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        boolean flag = num1>num2;
        System.out.println(flag);
    }
}
